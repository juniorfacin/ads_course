public class Endereco {
    String logradouro;
    int numero;
    String cidade;
    String estado;
    String cep;

    public Endereco(String logradouro, int numero, String cidade, String estado, String cep) {
        this.logradouro = logradouro;
        this.numero = numero;
        this.cidade = cidade;
        this.estado = estado;
        this.cep = cep;
    }
}
